package module02.DataTypes;

public class Boolean_module02 {

	public static void main(String[] args) {
		// primitive data type that can hold only two possible values: true or false
		boolean test1 = 5>2;
		System.out.print(test1); // result is True
		
		boolean test2 = 5<2;
		System.out.print(test2); // result is False

	}

}
