package example;

public class BaseEngineer {
	private String name;
	private String surname;
	private int experience;
	private int salary;
	private String degreeCheck;
	private String[] progLanguages;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSurname() {
		return surname;
	}
	public void setSurname(String surname) {
		this.surname = surname;
	}
	public int getExperience() {
		return experience;
	}
	public void setExperience(int experience) {
		this.experience = experience;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public String getDegreeCheck() {
		return degreeCheck;
	}
	public void setDegreeCheck(String degreeCheck) {
		this.degreeCheck = degreeCheck;
	}
	public String[] getProgLanguages() {
		return progLanguages;
	}
	public void setProgLanguages(String[] progLanguages) {
		this.progLanguages = progLanguages;
	}
	
	
}
